# - Actividad 1.1: Crear un programa que pida al usuario dos números y realice las cuatro
# operaciones matemáticas básicas (suma, resta, multiplicación y división),
# imprimiendo los resultados.
# Solicitar al usuario que ingrese dos números enteros
num1 = int(input("Ingrese un número: "));  # Primer número
num2 = int(input("Ingrese un segundo número: "));  # Segundo número

# Realizar y mostrar las operaciones básicas
print('La suma de los números ingresados es: ', num1 + num2);  # Suma
print('La resta de los números ingresados es: ', num1 - num2);  # Resta
print('La multiplicación de los números ingresados es: ', num1 * num2);  # Multiplicación
print('La división de los números ingresados es: ', num1 / num2);  # División