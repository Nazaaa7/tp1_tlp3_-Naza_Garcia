#Actividad 1.2: Hacer que el programa calcule el promedio de los dos números.
# Solicitar al usuario que ingrese dos números enteros
num1 = int(input("Ingrese un número: "));  # Primer número
num2 = int(input("Ingrese un segundo número: "));  # Segundo número

# Calcular el promedio sumando los números y dividiendo por 2
prom = (num1 + num2)/2;  # Cálculo del promedio aritmético

# Mostrar el resultado del promedio
print('El promedio de los números ingresados es: ', prom);  # Impresión del resultado